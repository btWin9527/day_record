function isValid(s: string): boolean {
    const map: Map<string, string> = new Map([
        [')', '('],
        [']', '['],
        ['}', '{']
    ]);
    const stack = [];
    for (const ch of s) {
        if (map.has(ch)) {
            if (!stack.length || stack[stack.length - 1] !== map.get(ch)) {
                return false
            }
            stack.pop()
        } else {
            stack.push(ch)
        }
    }
    return !stack.length
}

